frutas = ["maçã", "banana", "laranja", "uva", "manga"]
#          0        1         2         3       4


# Pegar do Indice 1 até o 3

print(frutas [1:3]) 

print(frutas[0:2])

# Pegar do índice 2 até o final
print(frutas[2:])      # ['laranja', 'uva', 'manga']

# Pegar TUDO (cópia completa!)
print(frutas[:])       # ['maçã', 'banana', 'laranja', 'uva', 'manga']